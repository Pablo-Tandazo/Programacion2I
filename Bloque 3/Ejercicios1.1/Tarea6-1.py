#   Ingrese dos numeros y haga la suma 

numero1=input("Ingresa el numero1= ")
numero2=input("Ingresa el numero2= ")

suma= int(numero1) + int(numero2)

Resta=int(numero1) - int(numero2)

Multiplicacion=int(numero1) * int(numero2)

Division=int(numero1) / int(numero2)

print(f"la respuesta de la suma es:{suma}, de la resta es :{Resta}, la multiplicacion es: {Multiplicacion}, la division es:{Division}")
