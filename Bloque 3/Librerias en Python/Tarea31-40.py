# 31 Lee un archivo de texto `datos.txt` y muestra las 3 palabras más repetidas utilizando un diccionario.
from collections import Counter

try:
    with open("datos.txt", "r", encoding="utf-8") as archivo:
        texto = archivo.read().lower()
        palabras = texto.split()
        top3 = Counter(palabras).most_common(3)

    print("Top 3 palabras más repetidas:")
    for palabra, veces in top3:
        print(f"{palabra}: {veces}")

except FileNotFoundError:
    print("El archivo 'datos.txt' no fue encontrado.")

